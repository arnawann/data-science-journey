print("===== STRING METHOD =====")

print("===== .upper =====")
name = "arnawan"
print(name.upper())

print("===== .lower =====")
job = "DATA SCIENTIST"
print(job.lower())

print("===== .title =====")
city = "jakarta selatan"
print(city.title())

print("===== .strip =====")
name = " Arnawan "
print(name.strip())

print("===== .replace =====")
sentence = "I love PHP"
print(sentence.replace("PHP", "Python"))

print("===== .split =====")
skills = "Python,SQL,Excel"
print(skills.split(","))

print("===== len() =====")
name = "Arnawan"
print(len(name))

print("===== .startswith() =====")
text = "Data Science"
print(text.startswith("Data"))

print("===== .endswith() =====")
filename= "report.csv"
print(filename.endswith(".csv"))
